        <br><br>
        <h2>1. Thu Thập Thông Tin</h2><br>
        Khi bạn truy cập Báo Mới VN, chúng tôi có thể thu thập các thông tin không nhận diện cá nhân như: địa chỉ IP, loại trình duyệt, thiết bị bạn sử dụng để truy cập. Ngoài ra, nếu bạn đăng ký nhận bản tin hoặc tạo tài khoản trên trang web, chúng tôi sẽ yêu cầu bạn cung cấp các thông tin cá nhân như: tên, địa chỉ email, số điện thoại.
        <br><br>
        <h2>2. Sử Dụng Thông Tin</h2><br>
        Thông tin cá nhân mà bạn cung cấp sẽ được sử dụng để:
        <ul>
            <li>Gửi các bản tin, cập nhật thông tin mới nhất từ Báo Mới VN.</li>
            <li>Hỗ trợ bạn khi có vấn đề liên quan đến tài khoản hoặc trải nghiệm người dùng.</li>
            <li>Cải thiện chất lượng dịch vụ, nội dung và giao diện trang web.</li>
        </ul>
