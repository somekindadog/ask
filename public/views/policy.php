        <br><br>
        <h2>4. Chia Sẻ Thông Tin</h2><br>
        Báo Mới VN cam kết không bán, trao đổi hay chia sẻ thông tin cá nhân của bạn với bất kỳ bên thứ ba nào, ngoại trừ các trường hợp sau:
        <ul>
            <li>Khi có yêu cầu từ các cơ quan pháp luật hoặc để tuân thủ các quy định pháp lý.</li>
            <li>Đối tác cung cấp dịch vụ cho chúng tôi nhưng phải đảm bảo tuân thủ các yêu cầu về bảo mật thông tin.</li>
        </ul>
        <br><br>
